export function searchProductos(filters: SearchProductosInput) {
  let filteredProductos = [...MOCK_PRODUCTOS]

  // Aplicar filtros
  if (filters.search) {
    const searchTerm = filters.search.toLowerCase()
    filteredProductos = filteredProductos.filter(
      (p) =>
        p.Nombredelproducto.toLowerCase().includes(searchTerm) ||
        p.Descripcion?.toLowerCase().includes(searchTerm) ||
        p.ClaveProducto.toLowerCase().includes(searchTerm),
    )
  }

  if (filters.tipo) {
    filteredProductos = filteredProductos.filter((p) => p.TipoProducto === filters.tipo)
  }

  if (filters.favorito !== undefined) {
    filteredProductos = filteredProductos.filter((p) => p.Favorito === filters.favorito)
  }

  if (filters.suspendido !== undefined) {
    filteredProductos = filteredProductos.filter((p) => p.Suspendido === filters.suspendido)
  }

  if (filters.grupoId) {
    filteredProductos = filteredProductos.filter((p) => p.GrupoProductoULID === filters.grupoId)
  }

  // Paginación
  const total = filteredProductos.length
  const totalPages = Math.ceil(total / filters.limit)
  const startIndex = (filters.page - 1) * filters.limit
  const endIndex = startIndex + filters.limit
  const productos = filteredProductos.slice(startIndex, endIndex)

  return {
    productos,
    total,
    page: filters.page,
    totalPages,
    limit: filters.limit,
  }
}
